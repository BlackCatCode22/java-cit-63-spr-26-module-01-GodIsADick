public class VarsAndMath {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        double price = 2.5;

        int sum = a + b;
        double total = sum * price;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("Sum = " + sum);
        System.out.println("Total cost = $" + total);
    }
}
